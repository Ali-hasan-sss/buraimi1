export function getNewsData(locale: string): NewsItem[] {
    const isAr = locale === 'ar';

    const categoryLabels = {
        ar: {
            events: 'فعاليات',
            academic: 'أكاديمي',
            research: 'بحث علمي',
            partnerships: 'شراكات',
        },
        en: {
            events: 'Events',
            academic: 'Academic',
            research: 'Research',
            partnerships: 'Partnerships',
        },
    } as const;

    return localizedNewsData.map((n) => ({
        id: n.id,
        title: isAr ? n.titleAr : n.titleEn,
        excerpt: isAr ? n.excerptAr : n.excerptEn,
        date: n.date,
        category: isAr ? categoryLabels.ar[n.category] : categoryLabels.en[n.category],
        image: n.image,
        featured: n.featured,
        readTime: isAr ? `${n.readTime} دقائق` : `${n.readTime} min`,
    }));
}

